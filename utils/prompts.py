def get_plan_prompt(paper_content: str) -> str:
    return f"""你是一个非常聪明的播客脚本策划者。你将获得一篇研究论文的文本，你的任务是为3个人讨论论文内容生成一个计划，这个讨论要非常吸引人、互动性强且充满热情。计划将只使用标题和要点来构建。播客应该遵循论文的结构。播客涉及以下人物：

**主持人**：他将以非常吸引人的方式介绍论文及其细节。非常专业、友好、温暖且充满热情。
**王同学**：他会对论文及其内容提出聪明且有意义的问题。他很好奇也很有趣。
**董老师**：他将对论文内容和其他相关主题提供深入的见解、评论和细节。他说话比其他两人少，他的发言更深入和详细, 但不会显得过于严肃，依然是日常口头话的风格。


论文内容: {paper_content}

请用中文回复播客计划的标题和要点:
# 播客计划标题:……
# 播客计划要点:……

"""

def get_initial_dialogue_prompt(paper_head: str) -> str:
    return f"""基于以下论文开头部分，生成一段主持人、董老师2人间的开场对话。对话应该介绍论文的基本信息,和核心内容，并激发听众的兴趣。

论文开头: {paper_head}

请用中文生成对话:
 **主持人**：……
 **董老师**：……
"""

def get_discussion_prompt(section: str, previous_dialogue: str) -> str:
    return f"""基于以下部分计划和之前的对话，继续生成一段主持人、王同学和董老师3人之间的对话。
    

部分计划: {section}
之前的对话: {previous_dialogue}

请用中文生成对话:
 **主持人**：……
 **王同学**：……
 **董老师**：……
"""

def get_enhance_prompt(draft_script: str) -> str:
    return f"""请优化以下播客脚本草稿，使其更流畅、更自然、更有吸引力。确保对话之间的过渡顺畅，并保持一致的语气和风格。严格遵循播客脚本草稿的结构和内容，保持只有3人对话。

脚本草稿: {draft_script}

请用中文输出优化后的脚本:
 **主持人**：……
 **王同学**：……
 **董老师**：……
""" 